export const formatOrders = (items) => {
  const format = items.map((order) => {
    return {
      id: Number(order._id),
      status:order.status,
      name:order.payer.name,
      email:order.payer.email,
      products:order.items,
      time:order.createdAt,
      payment:"Mercado Pago",
      price:order.__v
    }
  })
  return format;
}

